package sns.facebook.models;

public class Location {
  private String id;
  /**
   * name: city, country
   */
  private String name;

  public Location(String id, String name) {
    this.id = id;
    this.name = name;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }
}
